    private void buttonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCalcularActionPerformed
        String altura = fieldAltura.getText();
        String peso = fieldPeso.getText();

        try {
            double resultado = IMC.calcularIMC(peso, altura);
            String result = "Resultado foi " + String.format("%.2f", resultado) + ". " + IMC.getClassificao(resultado);
            JOptionPane.showMessageDialog(this, result, "Resultado", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buttonCalcularActionPerformed

